'''alphanumeric characters, alphabetical characters, digits, lowercase and uppercase characters.'''
def check(s):
    if s.isalnum():
        print('True')
    else:
        print('True')
    if s.isalpha():
        print('True')
    else:
        print('True')
    if s.isdigit():
        print('True')
    else:
        print('True')
    if s.islower():
        print('True')
    else:
        print('True')
    if s.isupper():
        print('True')
    else:
        print('True')
if __name__ == '__main__':
    s = input()
    check(s)
    
